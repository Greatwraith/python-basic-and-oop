# Gunakan "for loop" ketika ada serangkaian elemen yang ingin Anda ulangi.
# Dengan perulangan `for`, kita dapat mengeksekusi serangkaian pernyataan, 
# satu kali untuk setiap item dalam daftar, tuple, set, dll.




# Shopping bag data (list of items)
ShopChart = ['Apple', 'Banana', 'Grape', 'Orange']


for x in ShopChart: # Loop through every item inside the shopping bag
    
    # x = 1 fruit at a time from ShopChart
    print(x)   # print that fruit to the screen
    
    
# ShopChart = container that stores all fruits
# for x in ShopChart =
#   take one item from the list,
#   move to the next,
#   repeat until all items are printed


