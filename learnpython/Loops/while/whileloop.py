# WHILE LOOP
# Tell ur computer to continuously run your code, based on the value of a condition

#  Always ensure that you initialize all variables,
# before using them in loops to avoid errors and unexpected behavior.




#  1 INTRODUCTION TO WHILE LOOP

value = 0

while value < 7: #after this semicolon, it's the while loop's body
    print("not there yet, value=" + str(value)) #the print
    value = value +1 # the increment
print("value=" + str(value))
    
# While value is not greater than 7 yet, keep runing the print and the increment(from zero/0). 
# when it finally reached 7, stop it and print "value=..."








  
    
    