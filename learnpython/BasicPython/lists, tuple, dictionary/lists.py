x = ["Now", "we", "are", "cooking!"]



# cek tipe
type(x)  # <class 'list'>


# print the list
print(x)  # ['Now', 'we', 'are', 'cooking!']


# how many items in the list?
len(x)  # 4


# check if an item is in the list
"are" in x # true
"Today" in x # false


# accessing items in the list
print(x[0])  # Now
print(x[2])  # are


# slicing the list
print(x[1:3])  # ['we', 'are']



# slicing the list from index 1 to the end
print(x[1:])  # ['we', 'are', 'cooking!']








